#include "cub3d_bonus.h"

int	ft_mouse(int x, int y, t_all *s)
{
	(void)y;
	if (MOUSE && s->win.m != 10000)
		ft_rotate(s, 1, (x - s->win.m) / 5);
	s->win.m = x;
	return (0);
}
